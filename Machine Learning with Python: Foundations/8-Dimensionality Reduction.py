
# Topic: Dimensionality Reduction

# The process of reducing the number of features in a daset prior to modeling
# Reduces complexity and helps avoid the curse of dimensionality
# 
# TWo types of Dimensionality Reduction
#   - Feature Selection: The process of identifying the minimal set of features needed to build a good model
#                         Feature selection is also known as variable subset selection#
#   
#   - Feature Extraction: The use of mathemiatical functions to transform high-dimensional data into lower dimensions
#                         Very useful but values may be very difficult to interpret#